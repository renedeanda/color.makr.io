// Get color by name
export const getColorByName = (name) => {
  return namedColors.find(c => c.name.toLowerCase() === name.toLowerCase());
};

// Get colors by category
export const getColorsByCategory = (category) => {
  return namedColors.filter(c => c.category === category);
};

// Get all categories
export const getCategories = () => {
  return [...new Set(namedColors.map(c => c.category))].sort();
};

// Search colors by name
export const searchColors = (query) => {
  const q = query.toLowerCase();
  return namedColors.filter(c => c.name.toLowerCase().includes(q));
};

// Get random color
export const getRandomNamedColor = () => {
  return namedColors[Math.floor(Math.random() * namedColors.length)];
};

// Get similar colors (by hex similarity)
export const getSimilarColors = (hex, count = 5) => {
  const hexToNumber = (h) => parseInt(h.replace('#', ''), 16);
  const targetNum = hexToNumber(hex);

  return namedColors
    .map(color => ({
      ...color,
      distance: Math.abs(hexToNumber(color.hex) - targetNum)
    }))
    .sort((a, b) => a.distance - b.distance)
    .slice(0, count);
};
